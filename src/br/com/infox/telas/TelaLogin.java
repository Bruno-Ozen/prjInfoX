    public void logar() {
        String sql = "select * from tbusuarios where login = ? and senha = ?";
        try {
            // As linhas abaixo preparam a consulta ao banco em função do 
            // que foi digitado nas caixas de texto... O ? é substituído pelo
            // conteúdo das variáveis
            pst = conexao.prepareStatement(sql);
            pst.setString(1, txtareaUsuario.getText());
            String captura = new String(pfSenha.getPassword());
            pst.setString(2, captura);
            // A linha abaixo executa a query
            rs = pst.executeQuery();
            // Se existir usuário e senha correspondente
            if (rs.next()) {
                // A linha abaixo obtém o conteúdo do campo perfil da tabela tbusuarios
                String perfil = rs.getString(3);
                String usuario = rs.getString(2);
                TelaPrincipal principal = new TelaPrincipal();
                //System.out.println(perfil);
                // A estrutura abaixo faz o tratamento do perfil do usuario
                if(perfil.equals("admin")){
                    System.out.println("ADMIN");
                    principal.MenuRelatorio.setEnabled(true);
                    principal.MenuUsuarios.setEnabled(true);
                }else{
                    principal.MenuRelatorio.setEnabled(false);
                    principal.MenuUsuarios.setEnabled(false);
                }
                principal.setVisible(true);
                
                this.dispose();
                conexao.close();
            } else {
                JOptionPane.showMessageDialog(null, "Usuário e/ou senha inválido(s)");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

